# Tipos Int e float

# int -> número inteiro.
# O tipo int representa qualquer número.
# positivo ou negativo.

# float -> número com ponto flutuante.
# o tipo float representa qualquer número.
# positivo o negativo.

# A função type mostra o tipo de dado que o Python inferiu ao valor.

print(11) # int
print(-11) # int
print(0) # int

print(1.1) # float
print(1.1, 10.11) # float
print(0.0, -1.5) # float

print(type("Marchini")) 
print(type(1)) 
print(type(-1)) 
print(type("MARCHINI"), type(-1), type(0.0)) 

