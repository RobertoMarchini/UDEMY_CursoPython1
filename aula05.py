# Tipo de dado boll (boolean).
# Ao questionar algo em um programa. Só existem duas respostas possíveis:
# True ou False.
# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que questiona se o valor
# é igual a outro.

print(10 == 10)
print(10 == 11)
